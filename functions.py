phrase = "Giraffe Academy"
#upper and lowercase functions
print(phrase.lower())
print(phrase.upper())
#returns boolean
print(phrase.isupper())
print(phrase.upper().isupper())
#length of phrase
print(len(phrase))
#index
print(phrase[0])
print(phrase.index("a"))
#replace
print(phrase.replace("Giraffe", "Elephant"))